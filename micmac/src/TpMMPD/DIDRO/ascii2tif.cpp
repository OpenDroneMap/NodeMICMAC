// derawtisation de video thermique (optris et variocam): 1) surabondant (trop de frame) et 2) noisy (en particulier optris, effet de filé
// select only one frame among n consecutive  frame, choosen in compairing image bluriness
// export tiff only for these selected frames

int Test_ascii2tif_BlurinessSelect(int argc,char ** argv)
{

    string aDir = "./";
    string aPat, aPattern;
    int aSampling(32),aMinInter(5);
    bool aOptris(1), aDebug(0);
    Pt2di Sz(640,480);

    ElInitArgMain
    (
          argc,argv,
              LArgMain()  << EAMC(aPattern,"ascii image pattern, images generated either by irbis software (variocam hd thermal camera) or with SDKdirect optris library",eSAM_IsPatFile),
              LArgMain()
                          << EAM(aOptris,"Optris", true, "Are ascii generated by optris SDK direct script? Def true.")
                          << EAM(aSampling,"Sampling", true, "Pick up the best image every n images, n=Sampling")
                          << EAM(aMinInter,"Inter", true, "Minimum interval between two selected images, in number of images")
                          << EAM(Sz,"Sz", true, "Size of image")
                          << EAM(aDebug,"Debug", true, "Display more info in order to help debugging process")
    );

    SplitDirAndFile(aDir, aPat, aPattern);
    cInterfChantierNameManipulateur * aICNM = cInterfChantierNameManipulateur::BasicAlloc(aDir);
    vector<string>  aSetIm = *(aICNM->Get(aPat));

    int aOffset(0);
    std::cout << "Selection of the " << round_down(aSetIm.size()/aSampling) << " sharpest images \n";
    for (int aKSelImg=0; aKSelImg< round_down(aSetIm.size()/aSampling); aKSelImg++)
    {

       vector<Pt2dr> aVPair;
       // for each images in this image group, compute variance of laplacian as indicator of sharpness
       for (int aKImg=aKSelImg*aSampling+aOffset; aKImg<(aKSelImg+1)*aSampling; aKImg++)
       {
        string aImTxtName = aSetIm[aKImg];

        Im2D_REAL4 aIm(Sz.x,Sz.y);
        ImReadFromTxt(aImTxtName,aIm);
        if (aDebug) std::cout << "Compute Var for img " << aImTxtName << "...";
        double aVar = VarLap(&aIm);
        if (aDebug) std::cout << "done\n";
        Pt2dr aPair(double(aKImg), aVar);
        aVPair.push_back(aPair);
       }
       // choose the best image and export to tif file
       sortDescendPt2drY(aVPair);
       string aImTxtName = aSetIm[aVPair.at(0).x];
       cout << "+" << aImTxtName  << " selected             , var of laplacian " << aVPair.at(0).y << "\n";
       string aCom = "mm3d TestLib Ascii2Tif "
                           + aImTxtName
                           + " Optris=1 "
                           + " Debug=" + ToString(aDebug)
                           ;
       if (aDebug) std::cout << aCom << "\n";
       system_call(aCom.c_str());
       // ensure that successive images are not too close
       aOffset=(int) (aVPair.at(0).x+aMinInter - (aKSelImg+1)*aSampling);
       if (aOffset<0) aOffset=0;
       if (aDebug) std::cout <<" offset : " << aOffset << "\n";
    }

    return 0;
}



