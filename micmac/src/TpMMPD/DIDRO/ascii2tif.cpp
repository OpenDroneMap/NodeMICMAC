int ascii2tif(int argc,char ** argv)
{
    std::string aPat("Ree*.tif"), aColSep("\t");
    std::list<std::string> mLFile;
    bool aVario(true), aOutIsReal4(false);
    Pt2di aSz(1024,768);
    int aHLine(0);
    bool aC2K(false), aDebug(0), aOptris(0), aInt2Deg(0), aDeg2Int(0);

    ElInitArgMain
            (
                argc,argv,
                LArgMain()  << EAMC(aPat,"ascii image pattern, images generated either by irbis software (variocam hd thermal camera) or with SDKdirect optris library",eSAM_IsPatFile),
              LArgMain()
                << EAM(aVario,"Vario", true, "Are ascii generated by irbis soft for camera Variocam? Def true, exept if Optris=1.")
                << EAM(aC2K,"C2K", true, "Variocam: conversion from cercius value to kelvin value *100, in order to got integer value instead of float.")
                << EAM(aSz,"Sz", true, "Image size, default [1024,768] which is the size of variocam thermal imagery")
                << EAM(aOptris,"Optris", true, "Are ascii generated from optris camera? Def false. if true, set proper image size.")
                << EAM(aInt2Deg,"Int2Deg", true, "Optris: conversion of unsigned integer value (raw data) to degree?.")
                << EAM(aDeg2Int,"Deg2Int", true, "Optris: conversion of degree value to int2 raw data?.")
                << EAM(aHLine,"DropHL", true, "Number of Line to drop in the Header, default 10 (variocam ascii irbis soft)")
                << EAM(aColSep,"ColSep", true, "column separator, default= tabulation")
                << EAM(aDebug,"Debug", true, "Display much more info in order to help debugging process")
                 << EAM(aOutIsReal4,"ImReal4", true, "Output image is real4 tif images?")
                );
    if (aOptris) aVario=0;
    if (aVario & !EAMIsInit(&aC2K)) aC2K=true;
    if (aVario & !EAMIsInit(&aHLine)) aHLine=10;

    if (aOptris & !EAMIsInit(&aSz)) aSz=Pt2di(640,480);
    //if (aVario & !EAMIsInit(&aInt2Deg)) aInt2Deg=0;

    cInterfChantierNameManipulateur * aICNM = cInterfChantierNameManipulateur::BasicAlloc("./");
    // create the list of images starting from the regular expression (Pattern)
    mLFile = aICNM->StdGetListOfFile(aPat);

    std::cout << mLFile.size() << " ascii images detected\n";

    for (auto & aName : mLFile){

    std::cout << "Ascii to tif, image  " <<aName <<"\n";
    // crée une image Ram de taille adéquate

    int SzX(aSz.x), SzY(aSz.y);
    Im2D_REAL4 aImRAM(SzX,SzY);

    ImReadFromTxt(aName,aImRAM,aHLine,aVario,aColSep);

    if (aVario & aC2K)
    {

        std::cout << "conversion from celcius to Kelvin for Variocam images.\n";
        ELISE_COPY
                (
                    aImRAM.all_pts(),
                    100*(273.15+aImRAM.in()),
                    aImRAM.out()
                    );

    }

    if (aOptris & aInt2Deg)
    {

        std::cout << "conversion unsigned int Optris PI to degree value.\n";
        ELISE_COPY
                (
                    aImRAM.all_pts(),
                    (aImRAM.in()-1000)/10,
                    aImRAM.out()
                    );

    }

    if (aOptris & aDeg2Int)
    {

        std::cout << "conversion of degree value to unsigned int Optris PI raw value.\n";
        ELISE_COPY
                (
                    aImRAM.all_pts(),
                    (aImRAM.in()*10)+1000,
                    aImRAM.out()
                    );

    }


    std::string aNameTiff=aName.substr(0, aName.size()-3) + "tif";
    std::cout << "Writing of  " <<aNameTiff <<"\n";
    // sauve l'image

    if(aC2K | aInt2Deg | aOutIsReal4) {
    Tiff_Im aTiff
             (
                 aNameTiff.c_str(),
                 aImRAM.sz(),
                 GenIm::real4,
                 Tiff_Im::No_Compr,
                 Tiff_Im::BlackIsZero
             );

    ELISE_COPY
            (
                aImRAM.all_pts(),
                aImRAM.in(),
                aTiff.out()
                );
    } else {
        Tiff_Im aTiff
                 (
                     aNameTiff.c_str(),
                     aImRAM.sz(),
                     GenIm::int2,
                     Tiff_Im::No_Compr,
                     Tiff_Im::BlackIsZero
                 );

        ELISE_COPY
                (
                    aImRAM.all_pts(),
                    aImRAM.in(),
                    aTiff.out()
                    );

    }


    }

    return EXIT_SUCCESS;
}


template <class T,class TB>
void ImReadFromTxt(std::string aName, Im2D<T,TB> & aImOut, int aSkipLine,bool aVario,string colSep)
{
    // lecture du fichier ascii
    ifstream ascii(aName);
    std::string output;
    T aVal(0);
    // passe l'entête
    if (aSkipLine>0) for (int i(0); i<(aSkipLine-1);i++){ ascii >> output;}


    for (int l(0); l < aImOut.sz().y; l++)
    {
        // si le separator de colonne n'est pas une tabultation ou un espace, ca se gere pas de la meme manière
        if (colSep!="\t")
        {
            ascii >> output;
            //std::cout << "column separator is neither tab or space\n";
            char *aBuffer = strdup((char*)output.c_str());
            for (int col(0); col < aImOut.sz().x; col++)
            {
                Pt2di aPos(col,l);
                std::string aValStr("");
                // first call to strok not same syntax than other call
                if (col==0) {aValStr = strtok(aBuffer,colSep.c_str());} else {
                    aValStr = strtok( NULL, colSep.c_str() );
                }
                //std::cout << aValStr << "\n";
                FromString(aVal,aValStr);
                aImOut.SetR(aPos,aVal);
            }

        } else {

            for (int col(0); col < aImOut.sz().x; col++)
            {
                Pt2di aPos(col,l);
                // separator; tabulation
                ascii >> output;
                if (aVario) output[2]='.';
                FromString(aVal,output);
                aImOut.SetR(aPos,aVal);
            }
        }
    }
}


