double VarLap(Im2D_REAL4 * aIm2D)
{
    Im2D_REAL8 aLapl(3,3,
                        "0 1 0 "
                        "1 -4 1 "
                        " 0 1 0"
                   );

    Im2D_REAL8 aDenoise(3,3,
                        "0 1 0 "
                        "1 2 1 "
                        " 0 1 0"
                        );


    Pt2di aSzKer(round_up((aLapl.sz().x-1)/2), round_up((aLapl.sz().y-1)/2));


    Im2D_REAL4 aIm2D_DNs(aIm2D->sz().x, aIm2D->sz().y);
    // reduce noise

   // ELISE_COPY(aIm2D->all_pts(), GaussBlur(aIm2D->in_proj()), aIm2D_DNs.out());
     ELISE_COPY(aIm2D->all_pts(), som_masq(aIm2D->in(0),aDenoise), aIm2D_DNs.out());

    Im2D_REAL4 aIm2D_Lpl(aIm2D->sz().x, aIm2D->sz().y);

    ELISE_COPY(aIm2D_DNs.all_pts(),som_masq(aIm2D_DNs.in(0), aLapl), aIm2D_Lpl.out());

    double aVar = Variance(aIm2D_Lpl, 0, aSzKer);

    return aVar;
}




